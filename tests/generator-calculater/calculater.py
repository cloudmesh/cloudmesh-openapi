def add(x: float, y: float) -> float:
    """
    adding float and float.

    :param x: x value
    :type x: float
    :param y: y value
    :type y: float
    :return: result
    :return type: float
    """
    return x + y


def subtract(x: float, y: float) -> float:
    """
    subtract int and float sample.

    :param x: x value
    :type x: int
    :param y: y value
    :type y: float
    :return: result
    :return type: float
    """
    return x - y


def multiply(x: float, y: float) -> float:
    """
    Multiply float and float sample.

    :param x: x value
    :type x: int
    :param y: y value
    :type y: float
    :return: result
    :return type: float
    """
    return x * y


def division(x: float, y: float) -> float:
    """
    division float and float sample.

    :param x: x value
    :type x: int
    :param y: y value
    :type y: float
    :return: result
    :return type: float
    """
    return x / y
